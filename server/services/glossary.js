/**
 * Fix missing MCQ option labels.
 * Claude sometimes drops (a)(b)(c)(d) labels entirely, leaving bare option text.
 * Detects groups of 4 unlabeled lines before an "उत्तर:" answer line and adds labels.
 */
function restoreMissingOptionLabels(text) {
  if (!text) return text;
  const lines = text.split('\n');
  const result = [];

  for (let i = 0; i < lines.length; i++) {
    // Look for answer lines
    if (lines[i].match(/उत्तर\s*[:：]/)) {
      // Scan backwards for unlabeled option lines (skip empty lines)
      const optionCandidates = [];
      let j = i - 1;
      while (j >= 0 && optionCandidates.length < 4) {
        const trimmed = lines[j].trim();
        if (trimmed === '') {
          j--;
          continue;
        }
        // Already has option label (either "(a)" or "a)") — stop
        if (trimmed.match(/^\(?[a-d]\)/)) break;
        // Looks like a question text or heading (too long or has question patterns)
        if (trimmed.match(/^\d+[\.\)]/) || trimmed.match(/^निम्नलिखित|^कथन|^कूट|^नीचे दिए/)) break;
        optionCandidates.unshift({ index: j, text: trimmed });
        j--;
      }

      // If we found exactly 4 consecutive unlabeled option-like lines, add labels
      if (optionCandidates.length === 4) {
        const labels = ['(a)', '(b)', '(c)', '(d)'];
        for (let k = 0; k < 4; k++) {
          const idx = optionCandidates[k].index;
          const line = lines[idx];
          // Don't re-label if already labeled (either "(a)" or "a)" format)
          if (!line.trim().match(/^\(?[a-d]\)/)) {
            const leadingSpace = line.match(/^(\s*)/)[1];
            lines[idx] = leadingSpace + labels[k] + ' ' + line.trim();
          }
        }
      }
    }
    // Don't push here — we modify in-place and join at the end
  }

  return lines.join('\n');
}

/**
 * Remove duplicate MCQ options.
 * Claude sometimes outputs options BOTH inline in the question text
 * AND as separate lines below, causing every option to appear twice.
 *
 * Pattern detected:
 *   "...से है?  (a) मैंग्रोव और तटीय आर्द्रभूमि  (b) उष्णकटिबंधीय वर्षावन  (c) अंतःस्थलीय आर्द्रभूमि  (d) घास के मैदान और सवाना"
 *   a) मैंग्रोव और तटीय आर्द्रभूमि
 *   b) ...
 *   c) ...
 *   d) ...
 *
 * Fix: When a line contains all 4 inline options (a)...(b)...(c)...(d)...,
 * AND separate option lines follow, remove the inline options from the question line
 * and keep only the separate lines (which are properly formatted).
 */
function removeDuplicateOptions(text) {
  if (!text) return text;
  const lines = text.split('\n');

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];

    // Check if this line has ALL 4 inline options: (a)...(b)...(c)...(d)...
    const inlinePattern = /\(a\)\s*.+?\s*\(b\)\s*.+?\s*\(c\)\s*.+?\s*\(d\)\s*.+/;
    if (!inlinePattern.test(line)) continue;

    // Look ahead for separate option lines (a) or a) on their own lines
    let separateCount = 0;
    let j = i + 1;
    while (j < lines.length && j <= i + 8) { // look within next 8 lines
      const trimmed = lines[j].trim();
      if (trimmed === '') { j++; continue; }
      if (/^\(?[a-d]\)/.test(trimmed)) {
        separateCount++;
        j++;
      } else {
        break;
      }
    }

    // If we found at least 3 separate option lines after the inline ones, it's a duplicate
    if (separateCount >= 3) {
      // Strip the inline options from the question line — keep only text before (a)
      lines[i] = line.replace(/\s*\(a\)\s*.+?\s*\(b\)\s*.+?\s*\(c\)\s*.+?\s*\(d\)\s*.+/, '');
    }
  }

  return lines.join('\n');
}

/**
 * Fix broken answer format: "उत्तर: c)" → "उत्तर: (c)"
 * Claude sometimes drops the opening parenthesis in answer lines.
 */
function fixAnswerFormat(text) {
  if (!text) return text;
  // Fix "Answer: c)" or "Answer: (c)" → "उत्तर: (c)"
  let result = text.replace(/\bAnswer\s*[:：]\s*\(?([a-dA-D])\)?/g, 'उत्तर: ($1)');
  // Fix "उत्तर: c)" → "उत्तर: (c)"
  result = result.replace(/(उत्तर\s*[:：]\s*)([a-dA-D])\)/g, '$1($2)');
  return result;
}

/**
 * Format explanation/answer blocks by adding line breaks between statement analyses.
 * Turns "कथन 1 सही है... कथन 2 गलत है..." into separate lines for readability.
 */
function formatExplanationBreaks(text) {
  if (!text) return text;
  // Only apply to long paragraphs that look like explanations (multiple statement refs)
  const stmtCount = (text.match(/(?:कथन|Statement|विकल्प|Pair|युग्म)\s*\d/gi) || []).length;
  if (stmtCount < 2) return text;

  let result = text;
  // Add line break before "कथन 2", "कथन 3", etc. (Hindi) — but not "कथन 1" at start
  result = result.replace(/(?<!\n)\s+(कथन\s+(?:[2-9]|[1-9]\d))/g, '\n$1');
  // Add line break before "Statement 2", "Statement 3", etc. (English leftovers)
  result = result.replace(/(?<!\n)\s+(Statement\s+(?:[2-9]|[1-9]\d))/gi, '\n$1');
  // Add line break before "विकल्प 2", "विकल्प 3" (option analysis)
  result = result.replace(/(?<!\n)\s+(विकल्प\s+(?:[2-9]|[1-9]\d))/g, '\n$1');
  // Add line break before "युग्म 2" / "Pair 2" (match-the-following explanations)
  result = result.replace(/(?<!\n)\s+((?:युग्म|Pair)\s+(?:[2-9]|[1-9]\d))/gi, '\n$1');
  return result;
}

/**
 * Fix corrupted MCQ option labels.
 * Claude sometimes translates (a)/(b)/(c)/(d) into Hindi transliterations
 * like «एमसीक्यू1», "MCQ0", "MCQ1", etc. This restores them.
 */
function fixMCQLabels(text) {
  if (!text) return text;
  let result = text;

  // Map MCQ number patterns to correct option labels
  // Pattern: «एमसीक्यू{N}» or "MCQ{N}" or MCQ{N}" or «MCQ{N}» or "एमसीक्यू{N}"
  const mcqMap = {
    '0': '(a)', '1': '(b)', '2': '(c)', '3': '(d)',
    '4': '(a)', '5': '(b)', '6': '(c)', '7': '(d)',
    '8': '(a)', '9': '(b)', '10': '(c)', '11': '(d)',
    '12': '(a)',
  };

  // Fix «एमसीक्यू{N}» pattern (Hindi transliteration with guillemets)
  result = result.replace(/[«»""'\s]*एमसीक्यू(\d{1,2})[«»""'\s]*/g, (match, num) => {
    return (mcqMap[num] || `(${String.fromCharCode(97 + (parseInt(num) % 4))})`) + ' ';
  });

  // Fix "MCQ{N}" / MCQ{N}" / «MCQ{N}» patterns (English MCQ with quotes)
  result = result.replace(/[«»""'\s]*MCQ(\d{1,2})[«»""'\s]*/gi, (match, num) => {
    return (mcqMap[num] || `(${String.fromCharCode(97 + (parseInt(num) % 4))})`) + ' ';
  });

  // Fix केवल "MCQ1" 2 → (b) केवल 2 (where MCQ replaced option label before text)
  result = result.replace(/केवल\s*\(([a-d])\)\s*/g, '($1) केवल ');

  // Fix merged option lines: ensure space/newline before option labels
  result = result.replace(/([^\n(])(\([a-d]\)\s)/g, '$1\n$2');

  return result;
}

/**
 * Fix single English letter transliteration in match-the-following, tables,
 * person/variable names, coded answers, arrangement problems, and assertion-reason.
 *
 * Categories fixed:
 * - Match-the-following कूट: ए-3, बी-2 → A-3, B-2
 * - Person/variable labels: ए और बी → A और B
 * - Coded answer keys: ए बी सी डी → A B C D
 * - Arrangement sequences: एम, एन, ओ, पी, क्यू → M, N, O, P, Q
 * - Assertion (A) / Reason (R) labels
 * - Roman numeral I mistranslated as "मैं"
 * - Q transliterated as "प्र" or "प्रश्न" in labels
 * - PM/AM time transliteration
 */
function fixLetterTransliteration(text) {
  if (!text) return text;
  let result = text;

  // ── Hindi transliteration → English letter mapping ──────────────────────────
  const hindiToEnglish = {
    'ए': 'A', 'बी': 'B', 'सी': 'C', 'डी': 'D', 'ई': 'E',
    'एफ': 'F', 'जी': 'G', 'एच': 'H', 'आई': 'I', 'जे': 'J',
    'के': 'K', 'एल': 'L', 'एम': 'M', 'एन': 'N', 'ओ': 'O',
    'पी': 'P', 'क्यू': 'Q', 'आर': 'R', 'एस': 'S', 'टी': 'T',
    'यू': 'U', 'वी': 'V', 'डब्ल्यू': 'W', 'एक्स': 'X', 'वाई': 'Y',
    'ज़ेड': 'Z', 'ज़ी': 'Z',
  };

  // Sort entries longest-first to prevent partial matching (एम before ए, etc.)
  const sortedEntries = Object.entries(hindiToEnglish).sort((a, b) => b[0].length - a[0].length);

  // ── 1. Fix कूट (code) section: match-the-following answer grids ─────────────
  // Pattern: "ए  बी  सी  डी" as column headers or "ए-3, बी-2, सी-4, डी-1"
  // Look for lines with कूट nearby and fix letter patterns

  // Fix "ए-N, बी-N, सी-N, डी-N" pattern (with dash/hyphen + number)
  for (const [hindi, english] of sortedEntries) {
    // Letter followed by dash/hyphen and a number: ए-3 → A-3
    const dashPattern = new RegExp(`${hindi}\\s*[-–—]\\s*(\\d)`, 'g');
    result = result.replace(dashPattern, `${english}-$1`);

    // Letter followed by space and number in table grid: ए  3 → A  3
    // Only when it's clearly a table pattern (multiple letter-number pairs nearby)
    const spaceNumPattern = new RegExp(`${hindi}(\\s{2,})(\\d)`, 'g');
    result = result.replace(spaceNumPattern, `${english}$1$2`);
  }

  // ── 2. Fix standalone letter labels in parentheses ─────────────────────────
  // (ए) → (A), (बी) → (B), (आर) → (R) — for assertion/reason and other labels
  for (const [hindi, english] of sortedEntries) {
    const parenPattern = new RegExp(`\\(${hindi}\\)`, 'g');
    result = result.replace(parenPattern, `(${english})`);
  }

  // ── 3. Fix person/variable names: "ए और बी" → "A और B" ─────────────────────
  // Pattern: Hindi letter + और/or/व + Hindi letter (person name context)
  for (const [hindi1, eng1] of sortedEntries) {
    for (const [hindi2, eng2] of sortedEntries) {
      const andPattern = new RegExp(`${hindi1}\\s+(और|व|एवं|तथा)\\s+${hindi2}`, 'g');
      result = result.replace(andPattern, `${eng1} $1 ${eng2}`);
    }
  }

  // ── 4. Fix comma-separated letter sequences ─────────────────────────────────
  // "एम, एन, ओ, पी, क्यू" → "M, N, O, P, Q"
  // Match 3+ consecutive Hindi letters separated by commas
  // Sort longest-first to prevent partial matches (एम before ए, एफ before ए, etc.)
  const hindiLetters = Object.keys(hindiToEnglish).sort((a, b) => b.length - a.length);
  const hindiLetterGroup = hindiLetters.join('|');
  const seqPattern = new RegExp(
    `(${hindiLetterGroup})(?:\\s*,\\s*(${hindiLetterGroup})){2,}`,
    'g'
  );
  result = result.replace(seqPattern, (match) => {
    return match.replace(new RegExp(hindiLetterGroup, 'g'), (letter) => {
      return hindiToEnglish[letter] || letter;
    });
  });

  // ── 5. Fix column header rows in tables ─────────────────────────────────────
  // "     ए  बी  सी  डी" → "     A  B  C  D" (tab/space separated letters)
  const headerPattern = new RegExp(
    `^(\\s*)(${hindiLetterGroup})(\\s{2,})(${hindiLetterGroup})(\\s{2,})(${hindiLetterGroup})(\\s{2,})(${hindiLetterGroup})\\s*$`,
    'gm'
  );
  result = result.replace(headerPattern, (match, prefix, l1, s1, l2, s2, l3, s3, l4) => {
    return prefix +
      (hindiToEnglish[l1] || l1) + s1 +
      (hindiToEnglish[l2] || l2) + s2 +
      (hindiToEnglish[l3] || l3) + s3 +
      (hindiToEnglish[l4] || l4);
  });

  // ── 6. Fix coded answer strings like "आरएफसीपीसी" ──────────────────────────
  // Multi-letter Hindi transliterations glued together: आरएफसीपीसी → RFCPC
  // Parse greedily using longest-match approach
  const sortedHindiLetters = Object.keys(hindiToEnglish).sort((a, b) => b.length - a.length);

  function tryParseGluedLetters(str) {
    const letters = [];
    let pos = 0;
    while (pos < str.length) {
      let matched = false;
      for (const hindi of sortedHindiLetters) {
        if (str.startsWith(hindi, pos)) {
          letters.push(hindiToEnglish[hindi]);
          pos += hindi.length;
          matched = true;
          break;
        }
      }
      if (!matched) return null; // not a pure letter sequence
    }
    return letters.length >= 3 ? letters.join('') : null;
  }

  // Match runs of Devanagari characters that could be glued letter transliterations
  result = result.replace(/[\u0900-\u097F]{4,}/g, (match) => {
    const parsed = tryParseGluedLetters(match);
    return parsed || match;
  });

  // ── 7. Fix Roman numeral I → "मैं" ─────────────────────────────────────────
  // "मैं" means "I" (pronoun), but in UPSC context it's usually Roman numeral I
  // Note: \b doesn't work with Devanagari, so use lookaheads/lookbehinds or explicit boundaries
  // Fix patterns like "कथन मैं" → "कथन I", "मैं और II" → "I और II"
  result = result.replace(/मैं\s+(और|व|एवं|तथा)\s+(II|III|IV|V|VI)/g, 'I $1 $2');
  result = result.replace(/(II|III|IV|V|VI)\s+(और|व|एवं|तथा)\s+मैं(?=\s|$|,|।)/g, '$1 $2 I');
  // "कथन मैं" / "कथन मैं:" (statement I)
  result = result.replace(/(कथन|विकल्प|क्रमांक|संख्या|बिंदु)\s+मैं(?=\s|$|,|।|:)/g, '$1 I');
  // "केवल मैं" → "केवल I" in MCQ answer context
  result = result.replace(/केवल\s+मैं(?=\s|$|,|।)/g, 'केवल I');
  // "मैं, II और III" → "I, II और III"
  result = result.replace(/मैं\s*,\s*(II|III|IV)/g, 'I, $1');

  // ── 8. Fix Q → "प्रश्न" or "प्र" when it's a label ────────────────────────
  // In table contexts, Q is a column/person label, not "question"
  // "प्र" as a single label (not part of larger word)
  result = result.replace(/\bप्र\b(?=\s*[-–—]\s*\d)/g, 'Q');
  result = result.replace(/\(प्र\)/g, '(Q)');

  // ── 9. Fix PM/AM time transliteration ───────────────────────────────────────
  // "5 पीएम" → "5 PM", "अपराह्न" in time context → "PM"
  result = result.replace(/(\d+)\s*पीएम/g, '$1 PM');
  result = result.replace(/(\d+)\s*एएम/g, '$1 AM');
  // "अपराह्न N बजे" or "N बजे अपराह्न" patterns — keep Hindi but more natural
  result = result.replace(/(\d+)\s*अपराह्न/g, '$1 PM');
  result = result.replace(/अपराह्न\s*(\d+)/g, '$1 PM');
  result = result.replace(/(\d+)\s*पूर्वाह्न/g, '$1 AM');
  result = result.replace(/पूर्वाह्न\s*(\d+)/g, '$1 AM');

  // ── 10. Fix & → "और" when it's a separator in coded keys ──────────────────
  // Keep this light — only fix in clearly label contexts

  return result;
}

/**
 * Fix "के" being used where "K" (the letter) was intended.
 * Only in specific label contexts to avoid false positives.
 */
function fixLetterK(text) {
  if (!text) return text;
  // "के" followed by dash+number in code/table context
  return text.replace(/\bके\s*[-–—]\s*(\d)/g, 'K-$1');
}

/**
 * Apply Hindi-to-Hindi corrections for known systematic mistranslations.
 * Call this AFTER applyGlossaryPostProcessing.
 */
export function applyHindiCorrections(hindiText) {
  if (!hindiText) return hindiText;
  let result = hindiText;

  // Fix: garbled math/set symbols that shouldn't appear in Hindi exam text
  // These come from original DOCX special characters that survived translation
  result = result.replace(/\s*[∩∪∈∉⊂⊃⊆⊇∧∨⊕⊗⊙]\s*/g, ' – ');

  // Fix: "थाली" in cell biology context → "पट्टिका"
  // "भूमध्यरेखीय थाली" → "भूमध्यरेखीय पट्टिका" (equatorial/metaphase plate)
  result = result.replace(/भूमध्यरेखीय\s+थाली/g, 'भूमध्यरेखीय पट्टिका');
  result = result.replace(/मध्यवर्ती\s+थाली/g, 'मध्यवर्ती पट्टिका');
  result = result.replace(/कोशिका\s+थाली/g, 'कोशिका पट्टिका');

  // Remove duplicate options (inline + separate lines = double options)
  result = removeDuplicateOptions(result);

  // Normalize option labels: "a)" → "(a)", "b)" → "(b)", etc. on their own lines
  result = result.replace(/^(\s*)([a-d])\)\s+/gm, '$1($2) ');

  // Fix MCQ label corruption first (एमसीक्यू → (a)/(b)/(c)/(d))
  result = fixMCQLabels(result);

  // Restore missing option labels (Claude sometimes drops (a)(b)(c)(d) entirely)
  result = restoreMissingOptionLabels(result);

  // Fix broken answer format: "उत्तर: c)" → "उत्तर: (c)"
  result = fixAnswerFormat(result);

  // Format explanation blocks: add line breaks before "कथन 2", "कथन 3", "Statement 2", etc.
  // so explanations don't appear as a wall of text in the DOCX output
  result = formatExplanationBreaks(result);

  // Fix single English letter transliterations (ए→A, बी→B, etc.)
  result = fixLetterTransliteration(result);

  // Fix "के" → "K" in label contexts
  result = fixLetterK(result);

  // Apply known wrong→correct Hindi corrections
  for (const [wrong, correct] of HINDI_CORRECTIONS) {
    if (result.includes(wrong)) {
      result = result.split(wrong).join(correct);
    }
  }
  return result;
}

