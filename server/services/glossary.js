/**
 * Build the glossary string for the translation prompt.
 * Injects subject-specific terms first (HIGH PRIORITY), then base glossary.
 */
export function getGlossaryPrompt(subject = null) {
  // Compact format: "Term=Hindi" saves ~30% tokens vs "Term" → "Hindi"
  const baseEntries = Object.entries(UPSC_HCS_GLOSSARY)
    .map(([en, hi]) => `${en}=${hi}`)
    .join('\n');

  let subjectSection = '';
  if (subject && SUBJECT_GLOSSARIES[subject]) {
    const subjectEntries = Object.entries(SUBJECT_GLOSSARIES[subject])
      .map(([en, hi]) => `${en}=${hi}`)
      .join('\n');
    subjectSection = `## ${subject.toUpperCase()} GLOSSARY (apply these first):
${subjectEntries}

`;
  }

  return `${subjectSection}## TERMINOLOGY (use exact translations below — override your defaults):
${baseEntries}`;
}

/**
 * Apply glossary-based post-processing to translated output.
 * Corrects terminology after translation using find-and-replace.
 */
export function applyGlossaryPostProcessing(translatedText, originalText) {
  if (!translatedText || !originalText) return translatedText;

  const upperOriginal = originalText.toUpperCase();
  let result = translatedText;

  // Replace any remaining English terms with glossary Hindi
  // Sort by length (longest first) to avoid partial replacements
  const sortedEntries = Object.entries(UPSC_HCS_GLOSSARY)
    .sort((a, b) => b[0].length - a[0].length);

  for (const [englishTerm, hindiTerm] of sortedEntries) {
    if (!upperOriginal.includes(englishTerm.toUpperCase())) continue;
    const termRegex = new RegExp(`\\b${escapeRegex(englishTerm)}\\b`, 'gi');
    if (termRegex.test(result)) {
      // Avoid double-translation: skip if Hindi equivalent already appears right before this term
      // e.g. "सकल राष्ट्रीय उत्पाद (GNP)" → keep as-is, don't replace GNP again
      result = result.replace(termRegex, (match, offset) => {
        const before = result.substring(Math.max(0, offset - hindiTerm.length - 5), offset);
        if (before.includes(hindiTerm)) return match; // already translated nearby
        return hindiTerm;
      });
    }
  }

  return result;
}

/**
 * Parse custom abbreviation definitions from bookContext string.
 */
export function parseCustomAbbreviations(bookContext = '') {
  if (!bookContext) return [];
  const abbrs = [];
  const matches = bookContext.matchAll(/\b([A-Z]{2,8})\s*[=:]\s*\w/g);
  for (const match of matches) {
    abbrs.push(match[1]);
  }
  return abbrs;
}

/**
 * Apply user's custom glossary overrides to translated text.
 * Custom terms have highest priority — they override both Gemini output and built-in glossary.
 * @param {string} translatedText - Hindi text
 * @param {string} originalText - English source
 * @param {Array<{english_term: string, hindi_term: string}>} customTerms
 */
export function applyCustomGlossary(translatedText, originalText, customTerms) {
  if (!translatedText || !originalText || !customTerms?.length) return translatedText;

  let result = translatedText;
  const upperOriginal = originalText.toUpperCase();

  // Sort by English term length (longest first) to avoid partial matches
  const sorted = [...customTerms].sort((a, b) => b.english_term.length - a.english_term.length);

  for (const { english_term, hindi_term } of sorted) {
    if (!upperOriginal.includes(english_term.toUpperCase())) continue;
    // Replace any remaining English occurrences in the translated text
    const termRegex = new RegExp(`\\b${escapeRegex(english_term)}\\b`, 'gi');
    result = result.replace(termRegex, hindi_term);
  }

  return result;
}

function escapeRegex(str) {
  return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}
